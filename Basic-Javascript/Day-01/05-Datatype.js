// Datatypes in Javascript

let FatherAge = 34;
console.log(typeof FatherAge); //Integer

let FatherName = "Shaji Abraham";
console.log(typeof FatherName); //String

let FatherStatus = true;
console.log(typeof FatherStatus); //Boolean

let FatherDate;
console.log(typeof FatherDate); //Undefined

let FatherAccount = null;
console.log(typeof FatherAccount); // Null

let Age = BigInt(3922);

console.log(typeof Age); // BigInt

let Sym = Symbol(238);

console.log(typeof Sym); // Symbol


// There are Two types of Datatypes

// 1) Primitive Datatype ==> Integer,String,Null,Undefined,Boolean,Bigint,Symbol

// 2) Non-Primitive Datatype ==> Object,Array,Function