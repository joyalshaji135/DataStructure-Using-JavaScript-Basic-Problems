// Unary Operations

// There are Two Types of Unary Operators 1) Increment and 2) Decrement

// Post Increment And Decrement 
let inc = 1;

 // inc = inc + 1; post 

console.log("inc post = ",inc++); // out => 1
console.log("inc after post = ",inc); // out => 2


let dec = 1;

// dec = dec - 1; post

console.log("dec post = ",dec--); // out => 1
console.log("dec after post = ",dec); // out => 0

// Pre Increment and Decrement

let inc1 = 1;

// inc1 = 1 + inc1; pre

console.log("inc pre =  ",++inc1); // out => 2
console.log("inc after pre = ",inc1); // out => 2

let dec1 = 1;

// dec1 = 1 - dec1;

console.log("dec pre = ",--dec1); // out => 0
console.log("dec after pre = ",dec1); // out => 0
