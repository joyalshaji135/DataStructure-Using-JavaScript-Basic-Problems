//  Average of 10 number in while loop

let i = 1;
let avg = 0;
 while ( i <= 10 )
 {
    avg = avg + i;
    i++;
 }

let SumAvg = avg/10;
 console.log("Average of Ten Numbers :",SumAvg);

// Sum of 20 Numbers in While Loop

let SumOf = 0;
let count1 = 1;
while ( count1 <= 20 )
{
    SumOf = SumOf + count1;
    count1++;
}

console.log("Sum of 20 Numbers : ",SumOf);